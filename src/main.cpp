// utility structure for realtime plot
struct ScrollingBuffer {
    int MaxSize;
    int Offset;
    ImVector<ImVec2> Data;
    ScrollingBuffer(int max_size = 2000) {
        MaxSize = max_size;
        Offset = 0;
        Data.reserve(MaxSize);
    }
    void AddPoint(float x, float y) {
        if (Data.size() < MaxSize)
            Data.push_back(ImVec2(x, y));
        else {
            Data[Offset] = ImVec2(x, y);
            Offset = (Offset + 1) % MaxSize;
        }
    }
    void Erase() {
        if (Data.size() > 0) {
            Data.shrink(0);
            Offset = 0;
        }
    }
};

// utility structure for realtime plot
struct RollingBuffer {
    float Span;
    ImVector<ImVec2> Data;
    RollingBuffer() {
        Span = 10.0f;
        Data.reserve(2000);
    }
    void AddPoint(float x, float y) {
        float xmod = fmodf(x, Span);
        if (!Data.empty() && xmod < Data.back().x)
            Data.shrink(0);
        Data.push_back(ImVec2(xmod, y));
    }
};












void ShowScreen() {
    static ScrollingBuffer sdata1, sdata2;
    static RollingBuffer   rdata1, rdata2;
    ImVec2 mouse = ImGui::GetMousePos();
    static float t = 0.0f;
    t += ImGui::GetIO().DeltaTime;
    sdata1.AddPoint(t, mouse.x * 0.0005f);
    rdata1.AddPoint(t, mouse.x * 0.0005f);
    sdata2.AddPoint(t, mouse.y * 0.0005f);
    rdata2.AddPoint(t, mouse.y * 0.0005f);


    
    static float history = 5.0f;

    rdata1.Span = history;
    rdata2.Span = history;


 
    
    ImGui::Begin("Screen");
    ImGui::SliderFloat("Time", &history, 0.05f, 120.0f, "%.3f", ImGuiSliderFlags_None);
    ImGui::DragFloat("Fine ajust", &history, 0.05f, 0.05f, 120, "%.3f", ImGuiSliderFlags_None);
    if (ImPlot::BeginPlot("#Screen", ImVec2(-1, 250))) {
        ImPlot::SetupAxes(NULL, NULL, ImPlotAxisFlags_NoTickLabels, ImPlotAxisFlags_NoTickLabels);
        ImPlot::SetupAxisLimits(ImAxis_X1, t - history, t, ImGuiCond_Always);
        ImPlot::SetupAxisLimits(ImAxis_Y1, 0, 1);
        ImPlot::SetNextFillStyle(IMPLOT_AUTO_COL, 0.5f);
        ImPlot::PlotLine("Ch 1", &sdata1.Data[0].x, &sdata1.Data[0].y, sdata1.Data.size(), 0, 2 * sizeof(float));
        ImPlot::PlotLine("Ch 2", &sdata2.Data[0].x, &sdata2.Data[0].y, sdata2.Data.size(), 0, 2 * sizeof(float));
        ImPlot::EndPlot();
    }
    ImGui::End();
}






















